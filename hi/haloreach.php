Halo Reach is a first-person shooter made by 
Bungie and made specifically for the Xbox 360.
After Halo 3, Bungie split it in two different games Halo 3: ODST and Reach.
Reach was allowed a huge marketing budget and grossed 200 million on opening day.
Many said it was the best Halo yet.
<iframe width="560" height="315" src="http://www.youtube.com/embed/XqxqMwI4O0E" frameborder="0" allowfullscreen></iframe><br />
 The gameplay is amazing and you are Noble Six, a supersoldier
engaged in combat with the alien leader known as the Covenant.
Reach takes place in a futuristic science fiction setting and the year is 2552
 Humans, under the United Nations Space Command, have been waging a long war against the
 alien leader known as the Covenant. By the events of Reach, 
 All of Earth has almost been destroyed 
 Reach is an Earth except destroyed that is the UNSC's military hub.<br />
 <iframe width="560" height="315" src="http://www.youtube.com/embed/6n3Sq3NOz8g" frameborder="0" allowfullscreen></iframe><br />
Multiplayer is where it's at. You could do splitscreen or join a party 
and play online. There are many game modes to play but the best one is probably
Invasion because you can rank up extremely fast. Invasion is a 12 player mode
 with three squads of two on each team.The gametype puts Spartans against
 Elites. Elites fight for control of an area to disable a shield guarding a navigation core.
 Once the shield is disabled, they must transfer the core to a dropship. The Spartans have to stop them otherwise they will lose.
 As the game goes on, new things like vehicles become open. IGN and Gamespot rated this game at 95 and 91 respectively.
 Those scores are amazingly high. Many games have tried to match with Halo's awesomeness but only one has come close which is 
 the Call of Duty franchise. Halo just has a different feel. It's very realistic even though it takes place in the future. Here
