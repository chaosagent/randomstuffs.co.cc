<br />Battlefield 3 is a first-person shooter made by 
Ea digital illusions and published by electronic arts. Dice made a substancial role in the game as well. It was released on the xbox 360, ps3 and pc.
It is primary console is the ps3, while the Pc version has more features and hold a higher value.
Battlefield 3 is the next game after bad company 2 and sold 5 million copies in its first week.
It was designed as the call of duty killer, and many argued it has.
It received positive feedback and gained critically aclaimed reviews.<br />
<iframe width="560" height="315" src="http://www.youtube.com/embed/NDDfPxF3EFE" frameborder="0" allowfullscreen></iframe>
<br /> The gameplay is amazing and you step into the boots of a Force Recon Marine, an F-18 weapons system officer, an M1A2 Abrams tank operator, and a GRU operative. 
Battlefield 3 takes place in the near future, ranging from new york to iran.
 The campaign's format is similar to Call of duty black ops, with an interrogations and flashbacks of missions.
 However, it is generally the multiplayer that Battlefield 3 stands out as.
Players are given unscripted action and freedom unlike in the campaign.<br />
