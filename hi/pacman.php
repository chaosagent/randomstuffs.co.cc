The original Pacman was created by Toru Iwatani, an employee of Namco.
It was released in Japan on May 22, 1980.
He wanted to invent a game that appealed to a wide range of audiences.
<center><iframe width="420" height="315" src="http://www.youtube.com/embed/3-C7lHLFLU8" frameborder="0" allowfullscreen></iframe></center>
It was believed that he came up with Pacman's shape after taking a slice out of a pizza.
Later in an interview he stated that the shape actually came from rounding out the Japanse character for mouth, which is <i>kuchi.</i>
A perfect game ocurrs when a player successfully earns the max number of points possible in the first 255 levels and then as much possible on the 256th level.
This is due to a bug on that level that causes the right side of the screen to be corrupted with random letters and numbers that makes it impossible to eat all the pacdots and move on to the next level.
<center><img src=http://upload.wikimedia.org/wikipedia/en/d/da/Pac-Man_split-screen_kill_screen.png /></center>
Each ghost enemy has a different personality and a different pattern of moving around.
The red ghost chases you while the pink and blue ghosts try and go in front you.
The orange ghost's movement pattern is random;however, an analysis of the game showed that it mainly follows pacmanbut runs away when it gets close to you.
