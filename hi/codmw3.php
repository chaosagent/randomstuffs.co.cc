 Killstreaks don't play as big of a part in this game than MW2. A good thing is that killstreaks reset after you reach your maximum
 The variety has increased. Also if you do something with the objective like capture a flag or plant the bomb etc.
you can get a point towards your killstreak. There are three options of packages:
 Assualt, Support, and Specialist. Assualt is the same like every game where you have to earn a certain
